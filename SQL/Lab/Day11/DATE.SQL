--2.Write a query which is used to display employee names and also number of years 
  --those employees are working upto todays date from emp table
  --by using months_between() function?
SELECT ENAME, HIREDATE,
       FLOOR(MONTHS_BETWEEN(SYSDATE, HIREDATE)/12) AS YEARS_WORKING
  FROM EMP;

SELECT ENAME,HIREDATE,FLOOR(MONTHS_BETWEEN(SYSDATE,HIREDATE)/12) AS WORKING_YEARS FROM EMP;